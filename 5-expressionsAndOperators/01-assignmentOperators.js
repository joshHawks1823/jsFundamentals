// assignment operator
let x = 20;

// addition assignment operator
x += 1;
console.log(x);

// subtraction assignment operator

x -= 1;
console.log(x);

// mulitplication assignment operator

x *= 2;
console.log(x);

// division assignment operator

x /= 4;
console.log(x);

// remainder (modulus)
console.log(5 % 3);
console.log(25 % 7);

// exponential assignment operator
let y = 7;
y **= 2;
console.log(y);
